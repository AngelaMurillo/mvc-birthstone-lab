def choose_gem(date)
    date= date.split("/")
    month= date[0]
    if month == "01"
     return "Garnet"
    elsif month == "02"
     return "Amethyst"
    elsif month == "03"
     return "Aquamarine/Bloodstone"
    elsif month == "04"
     return "Diamond"
    elsif month == "05"
     return "Emerald"
    elsif month == "06"
     return "Pearl/Alexandrite/Moonstone"
    elsif month == "07"
     return "Ruby"
    elsif month == "08"
     return "Peridot"
    elsif month == "09"
     return "Sapphire"
    elsif month == "10"
     return "Tourmeline"
    elsif month == "11"
     return "Citrine/Yellow Topaz"
    elsif month == "12"
     return "Zircon/Turquoise"
    end
     
 
end

